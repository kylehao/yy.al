  var baseUrl = "https://cloud.hhhnn.com/d/189Family133/音乐视听/精选/";
  var playlist = [];

  for (var i = 0; i < rawFiles.length; i++) {
    var fileName = rawFiles[i];
    var info = parseSongInfo(fileName);
    playlist.push({
      title: info.title,
      artist: info.artist,
      mp3: baseUrl + encodeURIComponent(fileName) + ".mp3",
      poster: ""
    });
  }
  
  var cssSelector = {
    jPlayer: "#jquery_jplayer",
    cssSelectorAncestor: ".music-player"
  };
  
  var options = {
    swfPath: "http://cdnjs.cloudflare.com/ajax/libs/jplayer/2.6.4/jquery.jplayer/Jplayer.swf",
    supplied: "ogv, m4v, oga, mp3",
    errorAlerts: false,
    warningAlerts: false
  };
  
  var myPlaylist = new jPlayerPlaylist(cssSelector, playlist, options);

  // ============ 播放模式切换功能 ============
  // 0: 顺序播放, 1: 循环播放, 2: 随机播放
  var playMode = 0;
  var shuffleBtn = $('.icon-shuffle');
  var repeatBtn = $('.icon-repeat');

  // 更新按钮状态样式
  function updateModeButtons() {
    // 移除所有激活状态
    shuffleBtn.removeClass('active');
    repeatBtn.removeClass('active');
    
    if (playMode === 1) {
      // 循环播放 - 激活重复按钮
      repeatBtn.addClass('active');
    } else if (playMode === 2) {
      // 随机播放 - 激活随机按钮
      shuffleBtn.addClass('active');
    }
    // 模式0: 顺序播放 - 两个按钮都不激活
  }

  // 切换随机播放模式
  shuffleBtn.on('click', function(e) {
    e.preventDefault();
    if (playMode === 2) {
      // 如果当前是随机模式，切换到顺序播放
      playMode = 0;
    } else {
      // 否则切换到随机播放
      playMode = 2;
    }
    updateModeButtons();
    updatePlaylistLoop();
    showModeTip();
  });

  // 切换循环播放模式
  repeatBtn.on('click', function(e) {
    e.preventDefault();
    if (playMode === 1) {
      // 如果当前是循环模式，切换到顺序播放
      playMode = 0;
    } else {
      // 否则切换到循环播放
      playMode = 1;
    }
    updateModeButtons();
    updatePlaylistLoop();
    showModeTip();
  });

  // 更新播放列表的循环设置
  function updatePlaylistLoop() {
    if (playMode === 1) {
      // 循环播放：设置 loop 为 true
      myPlaylist.loop = true;
    } else {
      // 顺序播放或随机播放：loop 为 false
      myPlaylist.loop = false;
    }
  }

  // 显示当前模式提示
  function showModeTip() {
    var modeText = '';
    if (playMode === 0) {
      modeText = '?? 顺序播放';
    } else if (playMode === 1) {
      modeText = '?? 循环播放';
    } else if (playMode === 2) {
      modeText = '?? 随机播放';
    }
    
    // 创建或更新提示
    var tip = $('.mode-tip');
    if (tip.length === 0) {
      tip = $('<div class="mode-tip"></div>');
      $('.music-player').append(tip);
    }
    tip.text(modeText).fadeIn(200).delay(1200).fadeOut(400);
  }

  // 重写 jPlayerPlaylist 的 next 方法以支持随机播放
  var originalNext = myPlaylist.next;
  myPlaylist.next = function() {
    if (playMode === 2) {
      // 随机播放模式：随机选择一首歌
      var total = this.playlist.length;
      if (total <= 1) {
        this.play(0);
        return;
      }
      var randomIndex;
      do {
        randomIndex = Math.floor(Math.random() * total);
      } while (randomIndex === this.current && total > 1);
      this.play(randomIndex);
    } else {
      // 顺序或循环模式：调用原始方法
      originalNext.call(this);
    }
  };

  // 重写播放结束事件
  var originalPlaylistEnd = myPlaylist.playlistEnd;
  myPlaylist.playlistEnd = function() {
    if (playMode === 2) {
      // 随机模式：播放结束后自动播放下一首随机
      this.next();
    } else if (playMode === 1) {
      // 循环模式：重新播放当前歌曲
      this.play(this.current);
    } else {
      // 顺序模式：调用原始方法
      if (originalPlaylistEnd) {
        originalPlaylistEnd.call(this);
      }
    }
  };

